//Los operadores matemáticos nos dejan realizar operaciones básicas en nuestros datos.

let a = 2.5
let b = 5

console.log(a + b) //Adicion
console.log(a - b) //Sustraccion
console.log(a * b) //Multiplicación
console.log(a / b) //División
console.log(a ** b) //Potencia
console.log(a % b) //Módulo, resto o residuo
console.log(a++) //Incremento
console.log(a--) //Decremento

console.log(a += 2) //Asignación y suma
console.log(a -= 2) //Asignación y resta
console.log(a *= 2) //Asignación y multiplicación
console.log(a /= 2) //Asignación y división
console.log(a **= 2) //Asignación y potencia
console.log(a %= 2) //Asignación y residuo
