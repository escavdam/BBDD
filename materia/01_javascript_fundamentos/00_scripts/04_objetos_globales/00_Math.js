// Math es un objeto global con metodos y propiedades matemáticas y aritméticas

// Propiedades
  console.log(Math.PI); //Pi
  console.log(Math.E); //Euler
  console.log(Math.SQRT2); //Raiz cuadrada de 2
  console.log(Math.SQRT1_2); //Raiz cuadrada de 1/ 2
  console.log(Math.LN2); //Logaritmo natural de 2
  console.log(Math.LN10); //Logaritmo natural de 10
  console.log(Math.LOG2E); //Logaritmo base 2 de E
  console.log(Math.LOG10E); //Logaritmo base 10 de E

// Funciones
  console.log(Math.abs(-2)); //Valor absoluto
  console.log(Math.ceil(2.1)); //Redondeo hacia arriba
  console.log(Math.floor(2.9)); //Redondeo hacia abajo
  console.log(Math.round(2.5)); //Redondeo hacia arriba o abajo
  console.log(Math.round(2.4)); //Redondeo hacia arriba o abajo
  console.log(Math.pow(2, 3)); //Potencia
  console.log(Math.sqrt(9)); //Raiz cuadrada
  console.log(Math.sin(Math.PI / 2)); //Seno de 90 grados
  console.log(Math.cos(Math.PI / 2)); //Coseno de 90 grados
  console.log(Math.random()); //Numero aleatorio entre 0 y 1
  console.log(Math.random() * 10); //Numero aleatorio entre 0 y 10
  console.log(Math.random() * 100); //Numero aleatorio entre 0 y 100
  console.log(Math.random() * 100 + 1); //Numero aleatorio entre 1 y 101
  console.log(Math.random() * 100 + 50); //Numero aleatorio entre 50 y 151
